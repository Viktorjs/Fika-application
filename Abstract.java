
// creates abstract class
public abstract class CoffeeAbstract {
	// variables for the subclasses to inherit
	int high;
	int low;
	int RandomEnergy;

	// setters and getters for random energy value
	public int getRandomEnergy() {
		return RandomEnergy;
	}

	public void setRandomEnergy(int randomEnergy) {
		RandomEnergy = randomEnergy;
	}

}
